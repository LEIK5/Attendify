    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Save button
    String className = jTextField1.getText();
    String timeStart = jFormattedTextField1.getText();
    String timeEnd = jFormattedTextField2.getText();

    java.util.Date date = jDateChooser1.getDate();
    java.sql.Date dateOfSessions = new java.sql.Date(date.getTime());

    try {
        // Load the MySQL JDBC driver
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Create a connection to the database
        Connection conn = DriverManager.getConnection(dataConn, user, password);

        // Create a prepared statement to insert the data
        PreparedStatement pstmt = conn.prepareStatement("INSERT INTO classes (className, timeStart, timeEnd, dateOfSessions, section) VALUES (?, ?, ?, ?, ?)");

        // Set the parameters
        pstmt.setString(1, className);
        pstmt.setString(2, timeStart);
        pstmt.setString(3, timeEnd);
        pstmt.setDate(4, dateOfSessions);
        pstmt.setString(5, selectedSection); // Set the selected section

        // Execute the query
        pstmt.executeUpdate();

        // Close the connection
        conn.close();

        // Show a success message
        JOptionPane.showMessageDialog(this, "Class saved successfully!");
        jTextField1.setText("");
        jFormattedTextField1.setText("");
        jFormattedTextField2.setText("");

    } catch (ClassNotFoundException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Error saving data: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

