    private void updateAttendanceTable(String selectedSection) {
        attendanceTableModel.setRowCount(0);

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/attendify", "safdar", "123456");

            String query = "SELECT DISTINCT s.studentNumber, s.lastName, s.firstName, s.attendanceStatus, s.imagePath " +
                           "FROM students s " +
                           "JOIN classes c ON s.section = c.section " +
                           "WHERE s.section=?";
            PreparedStatement pstmt = conn.prepareStatement(query);
            pstmt.setString(1, selectedSection);

            ResultSet rs = pstmt.executeQuery();

            while (rs.next()) {
                String studentId = rs.getString("studentNumber");
                String lastName = rs.getString("lastName");
                String firstName = rs.getString("firstName");
                String attendanceStatus = rs.getString("attendanceStatus");
                String imagePath = rs.getString("imagePath");

                // Add data to the table model, including the image path
                attendanceTableModel.addRow(new Object[]{studentId, lastName, firstName, attendanceStatus, imagePath});
            }

            rs.close();
            pstmt.close();
            conn.close();

        } catch (ClassNotFoundException | SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching attendance records: " + e.getMessage());
        }
    }






