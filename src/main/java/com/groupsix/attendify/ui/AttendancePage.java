     private void populateComboBox() {
        try {
            // Load the MySQL JDBC driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Create a connection to the database
            Connection conn = DriverManager.getConnection(dataConn, user, password);

            // Query to fetch distinct sections from students table
            String query = "SELECT DISTINCT section FROM students";
            PreparedStatement pstmt = conn.prepareStatement(query);

            // Execute the query
            ResultSet rs = pstmt.executeQuery();

            // List to store sections
            List<String> sections = new ArrayList<>();

            // Iterate through result set and add sections to list
            while (rs.next()) {
                sections.add(rs.getString("section"));
            }

            // Close connection and statement
            rs.close();
            pstmt.close();
            conn.close();

            // Populate the JComboBox with sections
            for (String section : sections) {
                jComboBox1.addItem(section);
            }

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Error fetching sections: " + e.getMessage());
        }
    }

    


