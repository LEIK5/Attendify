private void populateSectionList() {
    try {
        // Load the MySQL JDBC driver
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Create a connection to the database
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/attendify", "safdar", "123456");

        // Query to fetch distinct class names, sections, and date inserted from classes table
        String query = "SELECT DISTINCT className, section, dateOfSessions FROM classes";
        PreparedStatement pstmt = conn.prepareStatement(query);

        // Execute the query
        ResultSet rs = pstmt.executeQuery();

        // Clear previous list
        sectionListModel.clear();

        // Iterate through result set and add class names with sections and date inserted to list
        while (rs.next()) {
            String className = rs.getString("className");
            String section = rs.getString("section");
            String dateInserted = rs.getString("dateOfSessions");
            sectionListModel.addElement(className + " - " + section + " - " + dateInserted);
        }

        // Close connection and statement
        rs.close();
        pstmt.close();
        conn.close();

    } catch (ClassNotFoundException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Error fetching class names, sections, and date inserted: " + e.getMessage());
    }
}




 private void sectionListValueChanged(javax.swing.event.ListSelectionEvent evt) {
    if (!evt.getValueIsAdjusting()) {
        String selectedValue = sectionList.getSelectedValue();
        if (selectedValue != null) {
            // Extract section from selectedValue
            String section = selectedValue.split(" - ")[1]; // Assumes the format "className - section - dateInserted"
            updateAttendanceTable(section);
        }
    }
}

